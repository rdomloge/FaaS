package com.example.faas.ex;

public class FunctionPreparationException extends FunctionException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 7638107502360721036L;

	public FunctionPreparationException() {
		super();
	}

	public FunctionPreparationException(String message, Throwable cause) {
		super(message, cause);
	}

	public FunctionPreparationException(String message) {
		super(message);
	}

	public FunctionPreparationException(Throwable cause) {
		super(cause);
	}

}
